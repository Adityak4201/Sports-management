    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/sports","root","root");
            Statement stmt = con.createStatement();
            String str = "Select * from sports_customer where customer_id="+jTextField1.getText()+";";
            ResultSet rs = stmt.executeQuery(str);
            if (rs.next())
            {
                jTextField2.setText(rs.getString("Customer_name"));
                jTextField3.setText(rs.getString("itemnumber"));
                jTextField4.setText(rs.getString("delivery_address"));
                jTextField5.setText(rs.getString("mobile"));
                jTextField6.setText(rs.getString("payment"));
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

