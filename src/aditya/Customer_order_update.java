    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pay_method=null;
        if(jRadioButton1.isSelected())
        pay_method="Cash On Delivery";
        if(jRadioButton2.isSelected())
        pay_method="Credit/Debit Card";
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/sports","root","root");
            Statement stmt = con.createStatement();
            String str = "Update sports_customer set customer_name ='"+jTextField2.getText()+"', item= '"+jComboBox1.getSelectedItem()+
            "', itemnumber ="+jTextField3.getText()+", delivery_address='"+jTextField4.getText()+"',mobile="+jTextField5.getText()
            +", payment= "+jTextField6.getText()+", pay_method ='"+pay_method+"' where customer_id ="+jTextField1.getText()+";";
            stmt.execute(str);
            JOptionPane.showMessageDialog(null,"Order Details Updated!!!!!");
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

